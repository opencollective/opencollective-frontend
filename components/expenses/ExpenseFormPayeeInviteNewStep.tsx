const PAYEE_TYPE = {
  USER: 'USER',
  ORG: 'ORG',
};

const Fieldset = styled.fieldset`
  border: none;
  padding: 0;
  margin: 0;
`;

const RadioOptionContainer = styled.label`
  align-items: center;
  display: flex;
  flex: 1 1 50%;
  font-size: 14px;
  font-weight: normal;
  line-height: 20px;
  margin: 0px;
  padding: 6px 16px;
  cursor: pointer;

  :not(:last-child) {
    @media (max-width: ${themeGet('breakpoints.0')}) {
      border-bottom: 1px solid #dcdee0;
    }
    @media (min-width: ${themeGet('breakpoints.0')}) {
      border-right: 1px solid #dcdee0;
    }
  }
`;

export const validateExpenseFormPayeeInviteNewStep = values => {
  const errors = requireFields(values, ['payee.name', 'payee.email']);
  if (!get(errors, 'payee.email')) {
    verifyEmailPattern(errors, values, 'payee.email');
  }
  return errors;
};

const ExpenseFormPayeeInviteNewStep = ({
  payeeFieldName = 'payee',
  recipientNoteFieldName = 'recipientNote',
  payoutMethodFieldName = 'payoutMethod',
  optionalPayoutMethod = false,
  formik,
  collective = null,
  onBack,
  onNext,
  hidePayoutDetails = false,
}) => {
  const intl = useIntl();
  const { formatMessage } = intl;
  const { values, touched, errors } = formik;
  const payeeValue = get(formik.values, payeeFieldName);
  const setPayoutMethod = React.useCallback(({ value }) => formik.setFieldValue(payoutMethodFieldName, value), []);
  const payeeType = payeeValue?.organization ? PAYEE_TYPE.ORG : PAYEE_TYPE.USER;
  const [showAdditionalInfo, setAdditionalInfo] = React.useState(
    !isEmpty(values.payeeLocation) || !isEmpty(get(values, payoutMethodFieldName)),
  );

  React.useEffect(() => {
    if (payeeValue?.organization?.name && !touched.payee?.organization?.slug) {
      const slug = suggestSlug(payeeValue.organization.name);
      if (payeeValue.organization.slug !== slug) {
        formik.setFieldValue(`${payeeFieldName}.organization.slug`, suggestSlug(payeeValue.organization.name));
      }
    }
  }, [payeeValue?.organization?.name]);

  const changePayeeType = e => {
    e.stopPropagation();
    const newPayeeType = e.target.value;
    if (newPayeeType === PAYEE_TYPE.USER) {
      formik.setFieldValue(payeeFieldName, omit(payeeValue, ['organization']));
    } else {
      formik.setFieldValue(payeeFieldName, { ...payeeValue, organization: { name: '' } });
    }
  };

  return (
    <Fragment>
      <StyledInputField label={formatMessage(msg.accountType)} labelFontSize="13px" mt={3}>
        <StyledCard>
          <Fieldset onChange={changePayeeType}>
            <Flex flexDirection={['column', 'row']} overflow="hidden">
              <RadioOptionContainer>
                <Box alignSelf={['center', 'baseline', null, 'center']} mr="16px">
                  <input
                    type="radio"
                    name="payeeType"
                    checked={payeeType === PAYEE_TYPE.USER}
                    value={PAYEE_TYPE.USER}
                    onChange={changePayeeType}
                    data-cy="payee-type-user"
                  />
                </Box>
                <Box>Personal Account</Box>
              </RadioOptionContainer>
              <RadioOptionContainer>
                <Box alignSelf={['center', 'baseline', null, 'center']} mr="16px">
                  <input
                    type="radio"
                    name="payeeType"
                    checked={payeeType === PAYEE_TYPE.ORG}
                    value={PAYEE_TYPE.ORG}
                    onChange={changePayeeType}
                    data-cy="payee-type-org"
                  />
                </Box>
                <Box>Organization Account</Box>
              </RadioOptionContainer>
            </Flex>
          </Fieldset>
        </StyledCard>
      </StyledInputField>

      {payeeType === PAYEE_TYPE.ORG && (
        <Fragment>
          <Grid gridTemplateColumns={['100%', 'calc(50% - 8px) calc(50% - 8px)']} gridColumnGap={[null, 2, null, 3]}>
            <Field name={`${payeeFieldName}.organization.name`}>
              {({ field }) => (
                <StyledInputField
                  name={field.name}
                  label={formatMessage(msg.orgNameLabel)}
                  labelFontSize="13px"
                  mt={3}
                  required
                >
                  {inputProps => <StyledInput {...inputProps} {...field} placeholder="e.g., Airbnb, Salesforce" />}
                </StyledInputField>
              )}
            </Field>
            <Field name={`${payeeFieldName}.organization.slug`}>
              {({ field }) => (
                <StyledInputField
                  mt={3}
                  labelFontSize="13px"
                  error={get(errors, `${payeeFieldName}.organization.slug`)}
                  name={field.name}
                  label={formatMessage(msg.orgSlugLabel)}
                >
                  {inputProps => <StyledInputGroup {...inputProps} {...field} prepend="opencollective.com/" />}
                </StyledInputField>
              )}
            </Field>
            <Field name={`${payeeFieldName}.organization.website`}>
              {({ field }) => (
                <StyledInputField
                  name={field.name}
                  label={formatMessage(msg.orgWebsiteLabel)}
                  labelFontSize="13px"
                  required={false}
                  mt={3}
                >
                  {inputProps => <StyledInputGroup {...inputProps} {...field} prepend="https://" />}
                </StyledInputField>
              )}
            </Field>

            <Field name={`${payeeFieldName}.organization.description`}>
              {({ field }) => (
                <StyledInputField
                  name={field.name}
                  label={formatMessage(msg.orgDescriptionLabel)}
                  labelFontSize="13px"
                  required={false}
                  mt={3}
                >
                  {inputProps => <StyledInput {...inputProps} {...field} placeholder="" />}
                </StyledInputField>
              )}
            </Field>
          </Grid>
        </Fragment>
      )}

      <Grid
        gridTemplateColumns={['100%', 'calc(50% - 8px) calc(50% - 8px)']}
        gridColumnGap={[null, 2, null, 3]}
        gridAutoFlow="dense"
      >
        <Box>
          <StyledInputFormikField
            name={`${payeeFieldName}.name`}
            htmlFor={`${payeeFieldName}.name`}
            required
            label={formatMessage(msg.nameLabel)}
            labelFontSize="13px"
            mt={3}
          >
            {({ field }) => <StyledInput {...field} />}
          </StyledInputFormikField>
        </Box>
        <Box>
          <StyledInputFormikField
            name={`${payeeFieldName}.email`}
            htmlFor={`${payeeFieldName}.email`}
            label={formatMessage(msg.emailTitle)}
            required
            labelFontSize="13px"
            mt={3}
          >
            {({ field }) => <StyledInput {...field} type="email" />}
          </StyledInputFormikField>
        </Box>

        {hidePayoutDetails ? null : !showAdditionalInfo ? (
          <Box gridColumn={[null, '1 / span 2']} mt={3}>
            <MessageBox type="info">
              <P fontSize="12px">{formatMessage(msg.additionalInfo)}</P>
              <P fontSize="12px" mt={2}>
                <StyledLinkButton onClick={() => setAdditionalInfo(true)}>
                  <FormattedMessage id="ExpenseForm.inviteAdditionalInfoBtn" defaultMessage="Add payout details" />
                </StyledLinkButton>
              </P>
            </MessageBox>
          </Box>
        ) : (
          <Fragment>
            <Box mt={3}>
              <StyledInputLocation
                onChange={values => {
                  formik.setFieldValue('payeeLocation', values);
                }}
                location={values.payeeLocation}
                errors={errors.payeeLocation}
                required={false}
              />
            </Box>
            <Box>
              <Field name={payoutMethodFieldName}>
                {({ field }) => (
                  <StyledInputField
                    name={field.name}
                    htmlFor="payout-method"
                    flex="1"
                    mt={3}
                    required={false}
                    label={formatMessage(msg.payoutOptionLabel)}
                    labelFontSize="13px"
                    error={
                      isErrorType(get(errors, payoutMethodFieldName), ERROR.FORM_FIELD_REQUIRED)
                        ? formatFormErrorMessage(intl, get(errors, payoutMethodFieldName))
                        : null
                    }
                  >
                    {({ id, error }) => (
                      <PayoutMethodSelect
                        inputId={id}
                        error={error}
                        onChange={setPayoutMethod}
                        payoutMethod={get(values, payoutMethodFieldName)}
                        payoutMethods={EMPTY_ARRAY}
                        payee={payeeValue}
                        disabled={!payeeValue}
                        collective={collective}
                        allowNull={optionalPayoutMethod}
                      />
                    )}
                  </StyledInputField>
                )}
              </Field>
              {get(values, payoutMethodFieldName) && (
                <Field name={payoutMethodFieldName}>
                  {({ field, meta }) => (
                    <Box mt={3} flex="1">
                      <PayoutMethodForm
                        fieldsPrefix={payoutMethodFieldName}
                        payoutMethod={field.value}
                        host={collective.host}
                        errors={meta.error}
                        required={false}
                      />
                    </Box>
                  )}
                </Field>
              )}
            </Box>

            <FastField name="invoiceInfo">
              {({ field }) => (
                <StyledInputField
                  name={field.name}
                  label={formatMessage(msg.invoiceInfo)}
                  labelFontSize="13px"
                  required={false}
                  mt={3}
                  gridColumn={1}
                >
                  {inputProps => (
                    <Field
                      as={StyledTextarea}
                      {...inputProps}
                      {...field}
                      minHeight={80}
                      placeholder={formatMessage(msg.invoiceInfoPlaceholder)}
                    />
                  )}
                </StyledInputField>
              )}
            </FastField>
          </Fragment>
        )}
      </Grid>
      <Box>
        <Field name={recipientNoteFieldName}>
          {({ field }) => (
            <StyledInputField
              name={field.name}
              label={formatMessage(msg.recipientNoteLabel)}
              labelFontSize="13px"
              required={false}
              mt={3}
            >
              {inputProps => <Field as={StyledTextarea} {...inputProps} {...field} minHeight={80} />}
            </StyledInputField>
          )}
        </Field>
      </Box>
      {payeeValue && (onBack || onNext) && (
        <Fragment>
          <StyledHr flex="1" mt={4} borderColor="black.300" />
          <Flex mt={3} flexWrap="wrap">
            {onBack && (
              <StyledButton
                type="button"
                width={['100%', 'auto']}
                mx={[2, 0]}
                mr={[null, 3]}
                mt={2}
                whiteSpace="nowrap"
                data-cy="expense-cancel"
                onClick={() => {
                  onBack?.();
                }}
              >
                ←&nbsp;
                <FormattedMessage id="Back" defaultMessage="Back" />
              </StyledButton>
            )}
            <StyledButton
              type="button"
              width={['100%', 'auto']}
              mx={[2, 0]}
              mr={[null, 3]}
              mt={2}
              whiteSpace="nowrap"
              data-cy="expense-next"
              buttonStyle="primary"
              onClick={e => {
                const isFormValid = reportValidityHTML5(e.target.form);
                const errors = validateExpenseFormPayeeInviteNewStep(values);
                if (!isEmpty(errors)) {
                  formik.setErrors(errors);
                } else if (isFormValid) {
                  onNext();
                }
              }}
            >
              <FormattedMessage id="Pagination.Next" defaultMessage="Next" />
              &nbsp;→
            </StyledButton>
          </Flex>
        </Fragment>
      )}
    </Fragment>
  );
};

ExpenseFormPayeeInviteNewStep.propTypes = {
  formik: PropTypes.object,
  payeeFieldName: PropTypes.string,
  recipientNoteFieldName: PropTypes.string,
  payoutMethodFieldName: PropTypes.string,
  payoutProfiles: PropTypes.array,
  onBack: PropTypes.func,
  onNext: PropTypes.func,
  hidePayoutDetails: PropTypes.bool,
  optionalPayoutMethod: PropTypes.bool,
  collective: PropTypes.shape({
    slug: PropTypes.string,
    type: PropTypes.string,
    host: PropTypes.shape({
      transferwise: PropTypes.shape({
        availableCurrencies: PropTypes.arrayOf(PropTypes.object),
      }),
    }),
    settings: PropTypes.object,
  }),
};

export default ExpenseFormPayeeInviteNewStep;
